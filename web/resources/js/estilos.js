function absoluto(elemento) {
    $(elemento).css({
        'position': 'absolute'
    });
}

function relativo(elemento) {
    $(elemento).css({
        'position': 'relative'
    });
}

function permanente(elemento) {
    $(elemento).css({
        'position': 'fixed'
    });
}

function estatico(elemento) {
    $(elemento).css({
        'position': 'static'
    });
}
